<h2>「自民」「維新」 ソーシャルならではの話題で盛り上がる<br>～「都議選」つぶやき分析第2弾～</h2>


<section>
<div class="pic"><img src="/corporate/souken/images/_pic/bigdata_01.png" width="296" height="198" alt=""></div>
株式会社プラスアルファ・コンサルティング（代表取締役：三室 克哉）は、ソーシャルリスニングツール「見える化エンジン」を使って、「東京都議会選挙」（6月23日投開票）に関するツイッターのつぶやきの分析を行ないました。6月17日に続いて今回2回目の都議選分析となります。<br />
当社の新サービス「選挙向けソーシャルメディア分析サービス」の分析アウトプットを用いて、6月12日から8日間のつぶやきの分析を行なった結果、告示後のつぶやき件数が、告示前と比べ4倍に急増したことが分かりました。<br />
「小児病院廃止問題」など告示前と同様の話題がある一方で、新たな話題もありました。告示後の話題では、自民党や日本維新の会の特定のつぶやきが、ツイッター上で話題になっていたことが分かりました。このように、つぶやきを分析することで、メディアでは取り上げられない話題を発見・分析することが出来ると考えられます<br />
<br />
■分析概要<br />
収集期間           ：2013年6月12日 ～ 6月19日　（8日間）<br />
収集条件           ：「都議選、都議会議員選挙、都議、都議会」 を含む全てのツイッターのつぶやき<br />
データ件数        ：84,481件<br />
前回の都議選分析：http://www.pa-consul.co.jp/pdf/130617_togisen_PAC.pdf<br />
<br />
<strong>１．基本分析： 時系列変化</strong><br />
<div class="pic"><img src="/corporate/souken/images/_pic/bigdata_02.png" width="443" height="399" alt=""></div>
<strong>（１）告示後につぶやき件数が4倍に</strong><br />
6月12日からの「都議選」に関するつぶやきを分析した結果、【上図上】のようになりました。<br />
分析の結果、14日の告示後につぶやき件数が急増していることが分かりました。<br />
告示後につぶやき件数が急増6月1日から11日までの同様の分析では、つぶやき件数が1日あたり約3, 000件だったのに対し、14日の告示後のつぶやきは、1日あたり約12,000件と、件数が告示前の4倍に伸びたことが分かりました。<br />
<br />
<strong>（２）自民党、日本維新の会に盛り上がり</strong><br />
次に政党別につぶやき件数の推移を見ると、【上図下】のようになりました。<br />
分析の結果、6月17日に自民党に関するつぶやきが3,149件と急増していることが分かりました。<br />
また、6月19日に日本維新の会に関するつぶやきが2,226件と急増していることが分かりました。<br />
次の政党別話題分析で、自民党と日本維新の会が、なぜ17日と19日につぶやき件数が伸びたかを特定しました。<br />
<br />
<strong>２．詳細分析：政党別・話題分析</strong><br />
次に各政党について、どのような話題がつぶやかれているかを、話題ごとに分類し分析しました。分析の結果、前回と同じ話題があった一方で、ツイッター上特有の新たに議論されている話題がいくつかあることが分かりました。<br />
<br />
<strong>（１）自民党：“「オタクの声は相手にしない」発言”が6月17日話題に</strong><br />
<div class="pic"><img src="/corporate/souken/images/_pic/bigdata_03.png" width="592" height="290" alt=""></div>
次の発言が、6月17日に自民党に関して多くつぶやかれていることが分かりました。<br />
“投票に行かない１００万のオタクの声よりも、選挙で１票を投じるＰＴＡのオバちゃんの声の方が、重視されるのが政治の世界。『都条例に反対してる連中は選挙にも行かない層（ｂｙ自民都議）』　この台詞はある意味重い。”（話題(1)(2)）<br />
<br />
<strong>（２）　民主党：「海江田代表・蓮舫氏による応援演説」が話題に</strong><br />
<div class="pic"><img src="/corporate/souken/images/_pic/bigdata_04.png" width="592" height="275" alt=""></div>
海江田代表の応援演説の聴衆が少なく、ヤジを浴びたという話題を麻生財務相の応援演説への人気と比較している話題（(1)）、蓮舫氏の演説の聴衆50人が足らずで、60代主婦が「威勢のいいこと言ったって、結局何もできなかったじゃない」という話題（(2)）が特徴的でした。<br />
小児病院廃止反対など、民主党が公約を守っていないという話題（(3)(4)(5)）も特徴的でした。<br />
<br />
<strong>（３）日本維新の会：「猪木氏ビンタ」、「選挙ポスターのキャッチコピー」「候補者がヘイトスピーチ応援」などが話題に</strong><br />
<div class="pic"><img src="/corporate/souken/images/_pic/bigdata_05.png" width="494" height="251" alt=""></div>
アントニオ猪木氏の応援演説の際のビンタを体罰と関連付けて語る話題（(1)）、　ポスターのキャッチコピーで「維新か。なれあいか」といいつつ都議会では与党だったことを批判する話題（(2)）、日本維新の会の候補が、ヘイトスピーチデモを応援していることを批判する話題（(3)(4)）がありました。<br />
6月19日に維新のつぶやき件数が伸びた理由は、最後の話題が拡散した影響でした。<br />
<br />
<strong>＜補足資料＞政策分析、年代・性別分析</strong><br />
<br />
都議選に関する話題を「経済・財政政策」「外交・防衛政策」「社会保障・教育政策」「震災復興・原発・防災・エネルギー政策」「地方分権・行政改革政策」の5つの政策分類で分析を行った結果、下図のようになりました。加えて、「見える化エンジン」のユーザプロファイル推定機能を用いて、ツイッターの発言者の年代、性別を推定し、分析しました。<br />
